// 1. Use a for loop to iterate through an array of names and log each name.
const names = ["Alice", "Bob", "Charlie", "David"];

for (let i = 0; i < names.length; i++) {
  console.log(names[i]);
}
// In this code, we use a for loop to iterate through the names array and log each name to the console.

// 2. Write a while loop that counts from 1 to 10 and logs the numbers.
let counter = 1;

while (counter <= 10) {
  console.log(counter);
  counter++;
}
// Here, we use a while loop to count from 1 to 10 and log each number to the console. The loop continues as long as counter is less than or equal to 10, and counter is incremented in each iteration.
